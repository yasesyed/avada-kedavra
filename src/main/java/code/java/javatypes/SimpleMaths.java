package code.java.javatypes;

public class SimpleMaths {
    public static void main(String[] args) {



        System.out.println("abs: " + Math.abs(-10));

        System.out.println("ceiling(10.1): " + Math.ceil(10.1));
        System.out.println("floor(10.6): " + Math.floor(10.6));

        System.out.println("rint(10.5): " + Math.rint(10.50));
        System.out.println("rint(10.51): " + Math.rint(10.51));

        System.out.println("round(10.49F): " + Math.round(10.49F));
        System.out.println("round(10.5): " + Math.round(10.5));

    }
}
